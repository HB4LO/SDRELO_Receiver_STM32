/* USER CODE BEGIN 4 */
void process_adc_buffer(uint32_t *buf, uint16_t len) {
    for (uint16_t i = 0; i < len; i++) {
        // Dual mode: ADC1 (I channel) is in the lower 16-bits
        // ADC2 (Q channel) is in the upper 16-bits (buf[i] >> 16)
        uint16_t adc1_val = buf[i] & 0xFFFF; // Get I channel

        // Update DC Offset using Exponential Moving Average
        dc_offset_i = (dc_alpha * (float)adc1_val) + ((1.0f - dc_alpha) * dc_offset_i);
        
        // Remove DC offset
        float centered = (float)adc1_val - dc_offset_i;
        
        // Convert to 16-bit signed integer (scale max internal range)
        int16_t sample_i = (int16_t)(centered * 16.0f); // Map 12-bit signed to 16-bit range

        push_audio_fifo(sample_i);
    }
}

void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc) {
    if (hadc->Instance == ADC1) {
        process_adc_buffer(&adc_dma_buf[0], ADC_DMA_BUF_SIZE / 2);
    }
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
    if (hadc->Instance == ADC1) {
        process_adc_buffer(&adc_dma_buf[ADC_DMA_BUF_SIZE / 2], ADC_DMA_BUF_SIZE / 2);
    }
}
