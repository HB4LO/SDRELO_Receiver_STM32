/* USER CODE BEGIN 0 */
void push_audio_fifo(int16_t sample) {
    uint16_t next = (audio_fifo_head + 1) % AUDIO_FIFO_SIZE;
    if (next != audio_fifo_tail) {
        audio_fifo[audio_fifo_head] = sample;
        audio_fifo_head = next;
        audio_fifo_count++;
    }
}

uint16_t get_audio_fifo_count(void) {
    if (audio_fifo_head >= audio_fifo_tail) {
        return audio_fifo_head - audio_fifo_tail;
    } else {
        return AUDIO_FIFO_SIZE - audio_fifo_tail + audio_fifo_head;
    }
}

int16_t pop_audio_fifo(void) {
    if (audio_fifo_head == audio_fifo_tail) return 0;
    int16_t val = audio_fifo[audio_fifo_tail];
    audio_fifo_tail = (audio_fifo_tail + 1) % AUDIO_FIFO_SIZE;
    audio_fifo_count--;
    return val;
}

/* function Setup_Tayloe_Mixer_LO
@brief: Setup the Tayloe Mixer LO
@param: freq_hz - frequency in Hz
@retval: None
*/

void Setup_Tayloe_Mixer_LO(uint32_t freq_hz)
{
	si5351PLLConfig_t pll_conf;
    si5351OutputConfig_t out_conf;

    // 1. Initialize the Si5351 module
    si5351_Init(0);

    // 2. Calculate the PLL and Multisynth parameters for IQ (90° offset)
    si5351_CalcIQ(freq_hz, &pll_conf, &out_conf);

    // 3. Setup Outputs FIRST to write the phase offsets into the registers.
    // Both use PLL_A, but the PLL isn't configured/reset until step 4.

    // Output 0 (In-Phase / I)
    si5351_SetupOutput(0,
                       SI5351_PLL_A,
                       SI5351_DRIVE_STRENGTH_8MA,
                       &out_conf,
                       0);

    // Output 1 (Quadrature / Q)
    si5351_SetupOutput(1,
                       SI5351_PLL_A,
                       SI5351_DRIVE_STRENGTH_8MA,
                       &out_conf,
                       (uint8_t)out_conf.div);

    // 4. Setup the PLL LAST.
    // This applies the PLL settings and triggers the vital PLL reset
    // across both channels simultaneously, locking in the 90° shift.
    si5351_SetupPLL(SI5351_PLL_A, &pll_conf);

    // 5. Enable CLK0 and CLK1 outputs simultaneously.
    si5351_EnableOutputs((1 << 0) | (1 << 1));
}
